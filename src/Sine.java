 public class Sine{
    public static void main(String[] args){
        int q=34;
        double c=(Math.toRadians(q));
        System.out.println(Math.sin(c));
        int r=90;
        double v=(Math.toRadians(r));
        System.out.println(Math.cos(v));
        int u=45;
        double b=(Math.toRadians(u));
        System.out.println(Math.tan(b));

    }
}